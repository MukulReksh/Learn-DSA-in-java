package DAY1;
/*
 * This chapter is all about basic operators
 */
public class _4Operators {
	 public static void main(String[] args) {
		 // 1.Arithmatic (+ - * /)
		 int num = 1;
		 int num2 = 2;
		 System.out.println(num + num2);//addittion
		 System.out.println(num - num2);//substraction
		 System.out.println(num * num2);//multiplication
		 System.out.println(num + num2);//divide
		 
		 //2.relational operators (> < == != >= <=)
		 //please note result of relational operator in boolean
		 System.out.println(num > num2);//greater than
		 System.out.println(num < num2);//less than
		 System.out.println(num == num2);//equal to
		 System.out.println(num != num2);//not equal to
		 System.out.println(num >= num2);//greater than equal to
		 System.out.println(num <= num2);//less than equal to
	 
		 //rest other operators we will cover in next chapter according to their use
	 }
}
